package upc.pxc.emergps;

public class Staff {
	String id;
	Double posx, posy;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public Double getPosx() {
		return posx;
	}
	public void setPosx(Double posx) {
		this.posx = posx;
	}
	public Double getPosy() {
		return posy;
	}
	public void setPosy(Double posy) {
		this.posy = posy;
	}


}
