package upc.pxc.emergps;

public class Staff {
	String id;
	Float posx, posy;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public Float getPosx() {
		return posx;
	}
	public void setPosx(Float posx) {
		this.posx = posx;
	}
	public Float getPosy() {
		return posy;
	}
	public void setPosy(Float posy) {
		this.posy = posy;
	}


}
